    private void tampilKonsultan() {
        model.getDataVector().removeAllElements();
        try {
            ResultSet rs = Model.Koneksi.st.executeQuery("select * from tbl_konsultan");
            while (rs.next()) {
                Object[] data = new Object[6];
                data[0] = (rs.getString(2));
                data[1] = (rs.getString(3));
                data[2] = (rs.getString(4));
                data[3] = (rs.getString(5));
                data[4] = (rs.getString(6));
                model.addRow(data);
            }

//ini blok while
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "eror tampil : " + e);
        } //ini blok catch
        bersih();
    }

    private void kode() {
        System.out.println();
        String splits;
        for (int i = 0; i < model.getRowCount(); i++) {
            splits = jTable1.getValueAt(i, 0).toString();
            String[] parts = splits.split("-");

            if (Integer.parseInt(parts[1]) != i + 1) {
                System.out.println(Integer.parseInt(parts[1]));
                if (i < 10) {
                    tfKode.setText("k-0" + Integer.toString(i + 1));
                    break;
                } else {
                    tfKode.setText("k-" + Integer.toString(i + 1));
                    break;
                }
            }
            if ((model.getRowCount() + 1)<10) {
            tfKode.setText("k-0" + Integer.toString(model.getRowCount() + 1));
            }else{
            tfKode.setText("k-" + Integer.toString(model.getRowCount() + 1));    
            }

        }

    }

