    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        try {
            if (tfGolongan.getText().equals(null) || tfTingkat.getText().equals(null)) {
                tfGolongan.setText("-");
                tfTingkat.setText("-");
            }
            if (cekData() == true) {
                Model.Koneksi.st.executeUpdate("insert into tbl_konsultan values (" + 0 + ",'" + tfKode.getText() + "','" + tfNama.getText() + "','" + tfJabatan.getText() + "','" + tfGolongan.getText() + "','" + tfTingkat.getText() + "')");
                JOptionPane.showMessageDialog(null, "Berhasil menyimpan");
                tampilKonsultan();
                aturTombol(false);
                kode();
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "eror Simpan : " + e);
        }
    }//GEN-LAST:event_btnSimpanActionPerformed

