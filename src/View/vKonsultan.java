    private boolean cekData() {
        boolean cek = true;
        if (tfNama.getText().equals("")) {
            cek = false;
        }
        if (tfJabatan.getText().equals("")) {
            cek = false;
        }
        if (cek == false) {
            JOptionPane.showMessageDialog(null, "Tolong Nama dan Jabatan Harus diIsi");
        }
        return cek;
    }

